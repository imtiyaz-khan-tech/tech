function creaetTable() {
    recs = recs.map(obj => {
        let newObj = {};
        for (let key in obj) {
            if (obj.hasOwnProperty(key)) {
                if (key === 'attributes') continue;
                if (typeof obj[key] === 'object' && obj[key] !== null && !Array.isArray(obj[key])) {
                    for (let innerKey in obj[key]) {
                        if (obj[key].hasOwnProperty(innerKey) && typeof obj[key][innerKey] !== 'object') {
                            newObj[`${key}.${innerKey}`] = obj[key][innerKey];
                        }
                    }
                } else {
                    newObj[key] = obj[key];
                }
            }
        }
        return newObj;
    });

    $('.tableContainer').html(createTableFromObjects(recs));
}


function createTableFromObjects(data) {
    if (!data.length) return "<p>No data available</p>";

    const columns = Object.keys(data[0]);
    console.log('$columns: ', columns);

    let table = "<table border='1' cellspacing='0' cellpadding='5'><thead><tr>";
    columns.forEach(col => {
        table += `<th>${col}</th>`;
    });
    table += "</tr></thead><tbody>";

    data.forEach(obj => {
        table += "<tr>";
        columns.forEach(col => {
            let val = obj[col];
            let dt = new Date(val);
            val = (!val || dt == 'Invalid Date' || typeof val == 'number' || (typeof val == 'string' && val.includes(':') == false ) ) ? val : getFormattedDateTime(dt);
            let clss = '';
            if (col == 'p66_Error_Message__c') {
                clss = 'wrap-colum';
            }
            table += `<td class="td ${clss}">${val ?? ""}</td>`;
        });
        table += "</tr>";
    });

    table += "</tbody></table>";
    document.title = 'Table [ ' + data.length + ']';
    return table;
}

